const withPatternMeta = (module: WorkflowModule): WorkflowModule => {
  const base =
    MODULE_PATTERN_META[module.id] ||
    (module.id >= 21 && module.id <= 35
      ? {
          openedFrom: "Linked initiating module",
          queueTechnique: "Initiation -> processing -> notification -> queue/bill",
          nextMove: "Update EHR, notify, queue/bill, and audit",
        }
      : null);

  if (!base) {
    return module;
  }

  return {
    ...module,
    ...base,
    stages: DETAILED_STAGE_OVERRIDES[module.id] ?? module.stages,
    coreFlowLoop: CORE_PATIENT_FLOW_LOOP,
  };
};

export const WORKFLOW_MODULES: WorkflowModule[] = modules.map(withPatternMeta);

export const WORKFLOW_MODULE_BY_ID = WORKFLOW_MODULES.reduce<Record<number, WorkflowModule>>((acc, mod) => {
  acc[mod.id] = mod;
  return acc;
}, {});
