export type FieldType =
  | "text"
  | "number"
  | "date"
  | "time"
  | "datetime-local"
  | "textarea"
  | "select"
  | "email"
  | "tel"
  | "file"
  | "checkbox";

export interface WorkflowField {
  key: string;
  label: string;
  type: FieldType;
  required?: boolean;
  options?: string[];
  note?: string;
}

export interface WorkflowForm {
  id: string;
  title: string;
  purpose: string;
  fields: WorkflowField[];
}

export interface WorkflowStage {
  id: string;
  label: string;
  description: string;
  vitalsCollected?: string;
  servicesProvided?: string;
  queueRouting?: string;
  interModuleLinks?: string;
}

